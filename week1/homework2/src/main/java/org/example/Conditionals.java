package org.example;

public class Conditionals {
    public static void main(String[] args) {
        int number = 24;
        if (number < 20){
            System.out.println("Number is lower than 20");
        } else if (number == 20){
            System.out.println("Number equals 20");
        } else {
            System.out.println("Number is greater than 20");
        }
    }
}
